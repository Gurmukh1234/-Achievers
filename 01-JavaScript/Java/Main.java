import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // Read rows and columns for the 2D array
        int rows = sc.nextInt();
        int cols = sc.nextInt();

        // Declare a 2D array
        int[][] num = new int[rows][cols];

        // Populate the array
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                num[i][j] = sc.nextInt();
            }
        }

        // Read the number to search in the array
        int x = sc.nextInt();

        // Search for the number and print the 2D array
        boolean found = false; // Flag to track if x is found
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                if (num[i][j] == x) {
                    System.out.println("Found at " + i + " " + j);
                    found = true; // Set the flag to true when found
                }
                System.out.print(num[i][j] + " "); // Print array elements
            }
            System.out.println(); // Print a new line after each row
        }

        if (!found) {
            System.out.println("Number not found");
        }
    }
}
